for lang in LANGUAGES:
    po_file = f"translations/{lang}/LC_MESSAGES/messages.po"
    print(f"Traitement de {lang}...")
    
    # Lire le fichier existant
    with open(po_file, 'r', encoding='utf-8') as f:
        content = f.read()
    
    # Ajouter les textes manquants
    added = 0
    for fr_text, en_text in COMMON_TEXTS.items():
        if f'msgid "{fr_text}"' not in content:
            # Utiliser la traduction spécifique ou l'anglais par défaut
            translation = SPECIFIC.get(lang, {}).get(fr_text, en_text)
            content += f'\nmsgid "{fr_text}"\nmsgstr "{translation}"\n'
            added += 1
    
    # Écrire le fichier
    with open(po_file, 'w', encoding='utf-8') as f:
        f.write(content)
    
    print(f"  ✅ Ajouté {added} textes")
    
    # Recompiler
    result = subprocess.run(['msgfmt', '-o', po_file.replace('.po', '.mo'), po_file], capture_output=True)
    if result.returncode == 0:
        size = os.path.getsize(po_file.replace('.po', '.mo'))
        print(f"  ✅ Compilé ({size} octets)")
    else:
        print(f"  ❌ Erreur: {result.stderr.decode()[:100]}")

print("\n✅ Terminé !")
